    <p style=" font-family: cursive" >Iron in your blood can oxidize resulting in damage to your cells and tissues. The increase in oxidative stress is most dangerous to your cardiovascular system. According to a new study published by the American Medical Association, giving blood every six months led to fewer heart attacks and strokes in test participants ages 43 to 61. Excessive iron is thought to contribute to heart disease, especially at its early stages. Donating blood on a regular basis reduces the iron stores in the body and this study supports the theory that reducing iron appears to preserve cardiovascular health. A second study of 2,682 men in Finland, reported in the American Journal of Epidemiology, found that men who donated blood at least once a year had an 88 percent lower risk of heart attacks than non-donors. This same group of researchers published a follow-up study and found that men who donated blood were less likely than non-donors to show any signs of cardiovascular disease.</p>
