<h3 style="font-family: cursive; text-align: center"> Whole Blood Donation</h3>

<p style="font-family: cursive"> Whole blood is the most flexible type of donation. It can be transfused in its original form, or used to help multiple people when separated into its specific components of red cells, plasma and platelets.  </p>

<h5 style="font-family: cursive">Who it helps: Whole blood is frequently given to trauma patients and people undergoing surgery.

</h5>

<h4 style="font-family: cursive">Time it takes: About 1 hour

</h4>
