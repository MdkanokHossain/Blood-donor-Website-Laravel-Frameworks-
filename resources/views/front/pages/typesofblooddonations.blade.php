<h3 style="font-family: cursive; text-align: center"> Power Red Donation
</h3>

<p style="font-family: cursive">During a Power Red donation, you give a concentrated dose of red cells, the part of your blood used every day for those needing transfusions as part of their care. This type of donation uses an automated process that separates your red blood cells from the other blood components, and then safely and comfortably returns your plasma and platelets to you.

    With just a little extra time at your appointment, you can donate more red cells and increase your impact on patients in need.   </p>




<h4 style="font-family: cursive">Who it helps: Red cells from a Power Red donation are typically given to trauma patients, newborns and emergency transfusions during birth, people with sickle cell anemia, and anyone suffering blood loss.



</h4>
<h5 style="font-family: cursive"> Time it takes: About 1.5 hours

</h5>

<h5 style="font-family: cursive"> Ideal blood types: O positive, O negative, A negative, and B negative



</h5>



<hr/>


<h3 style="font-family: cursive; text-align: center"> Platelet Donation

</h3>

<p style="font-family: cursive">Platelets are tiny cells in your blood that form clots and stop bleeding. Platelets are most often used by cancer patients and others facing life-threatening illnesses and injuries.

    In a platelet donation, an apheresis machine collects your platelets along with some plasma, returning your red cells and most of the plasma back to you. A single donation of platelets can yield several transfusable units, whereas it takes about five whole blood donations to make up a single transfusable unit of platelets.

    Platelets are collected at Red Cross donation centers only, and are not collected at blood drives.   </p>




<h4 style="font-family: cursive">Who it helps: Platelets are a vital element of cancer treatments and organ transplant procedures, as well as other surgical procedures.




</h4>
<h5 style="font-family: cursive"> Time it takes: About 2.5-3 hours



</h5>

<h5 style="font-family: cursive"> Ideal blood types: A positive, A negative, B positive, O positive, AB positive and AB negative




</h5>



<hr/>




<h3 style="font-family: cursive; text-align: center"> AB Elite Plasma Donation


</h3>

<p style="font-family: cursive">During an AB Elite donation, you give plasma, a part of your blood used to treat patients in emergency situations. AB plasma can be given to anyone regardless of their blood type. Plasma is collected through an automated process that separates plasma from other blood components, then safely and comfortably returns your red blood cells and platelets to you. AB Elite maximizes your donation and takes just a few minutes longer than donating blood.

</p>




<h4 style="font-family: cursive">Who it helps: AB Plasma is used in emergency and trauma situations to help stop bleeding.






</h4>
<h5 style="font-family: cursive"> Time it takes: About 1 hour and 15 minutes





</h5>

<h5 style="font-family: cursive"> Ideal blood types: AB positive, AB negative






</h5>
